static void render_board(void) {
  int c,e,s,x,y,z;
  for(x=1;x<=60;x++) for(y=1;y<=25;y++) {
    e=Grid(x,y).e;
    c=Grid(x,y).c;
    s=Grid(x,y).s;
    if(board.dark && !elem[e].f) {
      if(!world.light) goto dark;
      if(y<stat->y-4 || y>stat->y+4) goto dark;
      if(x<stat->x-radius[stat->y-y+4] || x>stat->x+radius[stat->y-y+4]) goto dark;
    }
    switch(e) {
      case 0: // EMPTY
        c=z=0;
        break;
      case 12: // DUPLICATOR
        if(stat[s].p1<6) z="\xFA\xFA\xF9\xF8\x6F\x4F"[stat[s].p1];
        else z=0xFA;
        break;
      case 13: // BOMB
        z=stat[s].p1>1?stat[s].p1+0x30:0x0B;
        break;
      case 15: // STAR
        c=c>14?9:c<9?15:c+1;
        z=0xB3;
        break;
      case 30: // TRANSPORTER
        if(stat[s].xd>0) z=')';
        else if(stat[s].xd<0) z='(';
        else if(stat[s].yd>0) z='v';
        else z='^';
        break;
      case 31: // LINE
        z=0;
        if(y==1 || Grid(x,y-1).e==31 || Grid(x,y-1).e==1) z|=1;
        if(y==25 || Grid(x,y+1).e==31 || Grid(x,y+1).e==1) z|=2;
        if(x==1 || Grid(x-1,y).e==31 || Grid(x,y).e==1) z|=4;
        if(x==60 || Grid(x+1,y).e==31 || Grid(x,y).e==1) z|=8;
        z="\xF9\xD0\xD2\xBA\xB5\xBC\xBB\xB9\xC6\xC8\xC9\xCC\xCD\xCA\xCB\xCE"[z];
        break;
      case 36: // OBJECT
        z=stat[s].p1;
        break;
      case 40: // PUSHER
        if(stat[s].xd==-1) z=0x11;
        else if(stat[s].xd==1) z=0x10;
        else if(stat[s].yd==-1) z=0x1E;
        else z=0x1F;
        break;
      default:
        if(e>46) {
          z=c;
          c=e==53?15:(e-46)*16+15;
        } else {
          z=elem[e].c;
        }
    }
    scrput(y-1,x-1,z,c);
    continue;
    dark: scrput(y-1,x-1,0xB0,0x07);
  }
}

static void make_message_area(void) {
  int i=(62-board.mlen)>>1;
  int n=0;
  scrput(24,i-1,32,0);
  while(n<board.mlen) scrput(24,i+n,board.msg[n],board.mess%7+9),n++;
  scrput(24,board.mlen+i,32,0);
}

int main(int argc,char**argv) {
  int i;
  opt['b']=-1;
  opt['c']=1;
  opt['w']=80;
  for(i=1;i<argc;i++) if(argv[i][0]) {
    if(argv[i][1]!='=') fatal("Improper option: %s\n",argv[i]);
    opt[argv[i][0]&127]=strtol(argv[i]+2,0,0);
  }
  i=get16();
  if(i==-1) {
    // A world file
    load_world();
    while(opt['b']--) {
      i=get16();
      if(i<0 || i>32767) fatal("Invalid board size\n");
      fread(buf,1,i,stdin);
    }
    get16();
  } else if(i>0) {
    // A board file
    world.health=100;
  } else {
    fatal("Invalid file format\n");
  }
  load_board();
  if(opt['d']) board.dark=0;
  if(opt['t'] && stat->x && stat->y && stat->x<=60 && stat->y<=25) {
    Grid(stat->x,stat->y).e=3;
    Grid(stat->x,stat->y).c=7;
  }
  render_board();
  if(opt['w']>60) make_status_area();
  if(board.mess) make_message_area();
  out_screen();
  return 0;
}

