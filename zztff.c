#define get8() fgetc(stdin)

static inline int get16(void) {
  int x=fgetc(stdin);
  x|=fgetc(stdin)<<8;
  if(x&0x8000) x-=0x10000;
  return x;
}

static inline void scrput(int y,int x,int ch,int co) {
  if(x>=0 && x<80 && y>=0 && y<25) {
    screen[y*160+x+x]=ch;
    screen[y*160+x+x+1]=co;
  }
}

static inline void scrputs(int y,int x,const unsigned char*t,int co) {
  while(*t) scrput(y,x++,*t++,co);
}

static void load_world(void) {
  int i;
  i=get16();
  if(opt['b']>i) fatal("Board number out of range (maximum is %d)\n",i);
  world.ammo=get16();
  world.gems=get16();
  for(i=1;i<8;i++) world.keys[i]=get8();
  world.health=get16();
  i=get16();
  if(opt['b']<0) opt['b']=i;
  world.torches=get16();
  world.light=get16();
  world.energy=get16();
  get16();
  world.score=get16();
  fread(buf,1,231,stdin);
  memcpy(world.title,buf+1,20);
  if(*buf<20) world.title[*buf]=0;
  world.time=get16();
  fread(buf,1,250,stdin);
}

static void load_board(void) {
  int i=0;
  int n;
  Cell c={0,0,255};
  fread(buf,1,51,stdin);
  while(i<1500) {
    n=get8()?:256;
    if(n<0) fatal("Unexpected end of file\n");
    c.e=get8();
    c.c=get8();
    while(n--) grid[i++]=c;
  }
  get8();
  board.dark=get8();
  fread(buf,1,5,stdin);
  board.mlen=get8();
  fread(board.msg,1,58,stdin);
  get16();
  board.time=get16();
  fread(buf,1,16,stdin);
  board.nstat=get16();
  if(board.nstat&~255) fatal("Incorrect number of stats\n");
  for(i=0;i<=board.nstat;i++) {
    stat[i].x=get8();
    stat[i].y=get8();
    stat[i].xd=get16();
    stat[i].yd=get16();
    get16();
    stat[i].p1=get8();
    stat[i].p2=get8();
    stat[i].p3=get8();
    fread(buf,1,12,stdin);
    n=get16();
    fread(buf,1,n>0?n+8:8,stdin);
    if(stat[i].x && stat[i].y && stat[i].x<=60 && stat[i].y<=25) grid[stat[i].x+stat[i].y*60-61].s=i;
    if(!stat[i].x && !stat[i].y) board.mess=stat[i].p2;
  }
}

